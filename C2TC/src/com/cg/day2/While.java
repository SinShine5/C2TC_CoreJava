package com.cg.day2;

public class While {

	public static void main(String[] args) {
		int i=0;
		// while loop is Entry check loop
		while(i<10) {
			i++;
			System.out.println(" ..");
			System.out.println("  -- ");
			System.out.println("  ,,");
			System.out.println("");
		}
		
	}

}
