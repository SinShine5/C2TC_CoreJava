package com.cg.day2;

public class IfElseLadder {

	public static void main(String[] args) {
		int i=17,j=13,k=5;
		if(i==1) {
			System.out.println("If statement is executed ");
			
		}
		else if(j==23) {
			System.out.println("1st Else If statement is executed ");
		}
		else if(k==5) {
			System.out.println("2nd Else If statement is executed ");
		}
		else {
			System.out.println(" Else statement is executed ");
		}
	}

}
